
	<h2>Ayuda</h2>
	<p>
		Bienvenido al nuevo servidor de música del ORFIS.
	</p>
	<p>
		Con la finalidad de brindar un mejor servicio, se ha desarrollado una nueva aplicación
		que actuará como servidor de música permitiendo nuevas funcionalidades.
	</p>

	<div class="alert alert-warning">
		Es obligatorio utilizar el navegador Google Chrome, Mozilla Firefox o Apple Safari para utilizar el servidor de música.
		No se garantiza el funcionamiento con Internet Explorer version 9 o anterior
	</div>

	<h3>Cómo escuchar música</h3>
	<p>
		Esta nueva aplicación de música se basa en la búsqueda.
	</p>
	<ol>
		<li>En la barra de búsqueda (parte superior) escriba el nombre del intérprete o canción que desee buscar y presione el botón buscar.</li>
		<li>De los resultados de la búsqueda, haga click en el botón 
			<span class="btn btn-default"><span class="glyphicon glyphicon-plus"></span></span> 
			que se encuentra en cada canción.
		</li>
		<li>Se agregará la canción a la lista de reproducción (parte inferior). En caso de que la lista esté vacía,
		empezará a sonar la canción.</li>
	</ol>

	<h3>La lista de reproducción</h3>
	<p>
		Las canciones que queramos escuchar se almacenarán en una lista de reproducción ubicada en la parte inferior
		de la página. <span class="text-danger">Esta lista estará disponible <strong>mientras tengamos abierta la página</strong>.</span>
		Si cerramos la página o el navegador, se perderá la lista de reproducción. Se puede cambiar de pestaña y la lista no se perderá.
		Se pueden agregar todas las canciones que queramos a la lista de reproducción y se pueden eliminar canciones de la lista.		
	</p>
	<p>Para abrir la lista de reproducción haga click sobre <strong>Lista de reproducción</strong> ubicada en la parte inferior de la página</p>
	<h4>Guardar mis listas de reproducción</h4>
	<p>
		Inicie sesión con su cuenta de red para poder guardar sus listas de reproducción y continuar
		escuchándolas cuando guste.
		Cuando inicia sesión se genera una lista de reproducción principal, esta es su lista de reproducción
		predeterminada aunque puede crear nuevas listas para organizar mejor su música.
	</p>
	<p>
		Para crear una lista de reproducción:
	</p>
	<ol>
		<li>Abra la lista de reproducción</li>
		<li>Haga click en el botón <span class="btn btn-default"><span class="glyphicon glyphicon-plus"></span></span> 
		que se encuentra junto al texto <strong>Mis listas de reproducción</strong></li>
		<li>Escriba el nombre de la canción y haga click en el botón verde</li>
		<li>Se creará la lista de reproducción, ahora podrá seleccionarla y agregrle canciones</li>
	</ol>

	<h3>Subir mi música</h3>
	<p>
		Se ha implementado la funcionalidad para subir las canciones que queramos que aparezcan en
		el servidor, esto facilitará el compartir música con nuestros compañeros y con esto podremos
		evitar tener música en nuestro equipo para que no se alente.
	</p>
	<p>Para subir música, siga el siguiente procedimiento</p>
	<ol>
		<li>En la pantalla principal, haga click en el botón <strong>Subir canciones</strong>
		ubicado en la parte superior derecha de la pantalla</li>
		<li>Lea bien las recomendaciones para subir archivos</li>
		<li>Haga click en el botón Seleccionar archivos</li>
		<li>Seleccione los archivos que quiere subir (puede seleccionar más de 1, se recomiendan menos de 100) y haga click en Aceptar</li>
		<li>Se subirán los archivos, espere a que se suban, si desea cancelar alguno, haga click en su respectivo botón cancelar</li>
		<li>Una vez finalizado, podrá escuchar las canciones en el reproductor</li>
	</ol>
